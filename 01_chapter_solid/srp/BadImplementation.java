public class Person {
    String name;
    int years;

    public Person(String name, int years) {
        this.name = name;
        this.years = years;
    }

    public String say(String phrase) {
        return phrase;
    }

    public int sumTwoIntNumbers(int a, int b) {
        return a + b;
    }

    public double divideTwoIntNumbers(int a, int b) {
        return a / b;
    }
}
